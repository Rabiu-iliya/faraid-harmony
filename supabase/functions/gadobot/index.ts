import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are GadoBot, an expert Islamic inheritance (Faraid) assistant for the GadoPro application. You help users understand:

1. **Fixed Shares (Fard)**: Quranic shares - 1/2, 1/4, 1/8, 1/3, 2/3, 1/6
2. **Blocking Rules (Ḥajb)**: Which heirs block others:
   - Children block siblings from inheritance
   - Son blocks grandson
   - Father blocks siblings and grandfather
   - Full siblings block paternal siblings
   - Maternal siblings are blocked when father exists
3. **Residuary (ʿAsabah)**: How remainder is distributed, males get 2x female share
4. **ʿAwl**: When total fixed shares exceed the estate, all shares are proportionally reduced
5. **Radd**: When total shares are less than estate and no residuary heirs, surplus is redistributed to eligible heirs (excluding spouse)

Answer clearly in the user's language. Use Islamic terminology with simple explanations. Be respectful and scholarly. Reference Quranic verses (Surah An-Nisa 4:11-12) when relevant.`,
          },
          ...messages,
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const data = await response.json();
    return new Response(JSON.stringify(data), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("gadobot error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
